package chapter14.arrayList;

public class Person {
	
	//한 사람의 정보를 담당하는 클래스
	private String name;
	private int age;
	private String tell;
	
	
	//생성자 생략
	
	//메서드	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getTell() {
		return tell;
	}
	public void setTell(String tell) {
		this.tell = tell;
	}
	

}
